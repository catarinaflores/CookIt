<div id="Recepie">
    <h2>Omelette</h2>
    <h3>Ingredients</h3>
    <ul>
        <li>Eggs</li>
        <li>Sausage</li>
    </ul>
    <h3>Instructions</h3>
    <ol>
        <li>Crack 2 eggs.</li>
        <li>Whisk the eggs.</li>
        <li>Pour the eggs into a hot pan.</li>
        <li>Cook until the eggs are set.</li>
        <li>Cut the sausage into small pieces.</li>
        <li>Add the sausage to the eggs.</li>
        <li>Fold the omelette in half.</li>
        <li>Cook for another minute.</li>
    </ol>
</div>

